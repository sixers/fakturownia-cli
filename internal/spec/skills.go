func validateSkillBundle(bundle SkillBundleSpec) error {
	if strings.TrimSpace(bundle.Name) == "" {
		return fmt.Errorf("skill bundle name is required")
	}
	if _, err := skillCommandAssignments(bundle); err != nil {
		return err
	}
	expected := make([]SkillCommandRef, 0, len(Registry()))
	for _, command := range Registry() {
		expected = append(expected, SkillCommandRef{Noun: command.Noun, Verb: command.Verb})
	}
	assignments, err := skillCommandAssignments(bundle)
	if err != nil {
		return err
	}
	for _, ref := range expected {
		if _, ok := assignments[ref]; !ok {
			return fmt.Errorf("command %s %s is not assigned to any skill area", ref.Noun, ref.Verb)
		}
	}
	for _, area := range bundle.Areas {
		if strings.TrimSpace(area.Name) == "" {
			return fmt.Errorf("skill area %s is missing a name", area.Key)
		}
		if strings.TrimSpace(area.Description) == "" {
			return fmt.Errorf("skill area %s is missing a description", area.Key)
		}
		if area.Prerequisite != "" {
			if _, ok := skillAreaByKey(bundle, area.Prerequisite); !ok {
				return fmt.Errorf("skill area %s has unknown prerequisite %s", area.Key, area.Prerequisite)
			}
		}
		for _, related := range area.RelatedSkills {
			if _, ok := skillAreaByKey(bundle, related); !ok {
				return fmt.Errorf("skill area %s has unknown related skill %s", area.Key, related)
			}
		}
	}
	return nil
}
