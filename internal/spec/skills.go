func skillAreaByKey(bundle SkillBundleSpec, key string) (SkillAreaSpec, bool) {
	for _, area := range bundle.Areas {
		if area.Key == key {
			return area, true
		}
	}
	return SkillAreaSpec{}, false
}

func commandSpecForSkillRef(ref SkillCommandRef) (CommandSpec, error) {
	spec, ok := FindCommand(ref.Noun, ref.Verb)
	if !ok {
		return CommandSpec{}, fmt.Errorf("unknown skill command reference %s %s", ref.Noun, ref.Verb)
	}
	return spec, nil
}

func skillCommandAssignments(bundle SkillBundleSpec) (map[SkillCommandRef]string, error) {
	assignments := make(map[SkillCommandRef]string)
	for _, area := range bundle.Areas {
		for _, ref := range area.CommandRefs {
			if _, err := commandSpecForSkillRef(ref); err != nil {
				return nil, err
			}
			if owner, ok := assignments[ref]; ok {
				return nil, fmt.Errorf("skill command reference %s %s assigned to both %s and %s", ref.Noun, ref.Verb, owner, area.Key)
			}
			assignments[ref] = area.Key
		}
	}
	return assignments, nil
}

func commandsForSkillArea(area SkillAreaSpec) ([]CommandSpec, error) {
	specs := make([]CommandSpec, 0, len(area.CommandRefs))
	for _, ref := range area.CommandRefs {
		spec, err := commandSpecForSkillRef(ref)
		if err != nil {
			return nil, err
		}
		specs = append(specs, spec)
	}
	return specs, nil
}

func exampleCommandsForSkillArea(area SkillAreaSpec) ([]CommandSpec, error) {
	refs := area.CommandRefs
	if len(area.ExampleRefs) > 0 {
		refs = area.ExampleRefs
	}
	seen := make(map[SkillCommandRef]struct{}, len(refs))
	specs := make([]CommandSpec, 0, len(refs))
	for _, ref := range refs {
		if _, ok := seen[ref]; ok {
			continue
		}
		seen[ref] = struct{}{}
		spec, err := commandSpecForSkillRef(ref)
		if err != nil {
			return nil, err
		}
		specs = append(specs, spec)
	}
	return specs, nil
}

